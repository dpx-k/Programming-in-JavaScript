// you can generate random numbers in java 

console.log(Math.random()) // generates a random number between 0 - 0.99 

// multiply it by 10 and truncate it to get a random integer between 1 and 10 
// you can extend the range by multiplying it with powers of 10 
console.log(Math.trunc(Math.random() * 10))
console.log(Math.trunc(Math.random() * 100))
console.log(Math.trunc(Math.random() * 1000))
