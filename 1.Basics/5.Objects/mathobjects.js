console.log(Math.PI) // pi 
console.log(Math.E) // euler's constant
console.log(Math.LN2) // natural logarithm of 2 

console.log(Math.ceil(Math.PI)); // Rounds π (3.141...) up to 4  
console.log(Math.floor(Math.PI)); // Rounds π (3.141...) down to 3  
console.log(Math.round(Math.PI)); // Rounds π (3.141...) to the nearest integer = 3  
console.log(Math.trunc(1/3)); // Truncates (removes decimal) 1/3 = 0  


console.log(Math.pow(2,3)); // 2^3 = 8  
console.log(Math.sqrt(16)); // Square root of 16 = 4  
console.log(Math.cbrt(8)); // Cube root of 8 = 2  
console.log(Math.abs(-10)); // Absolute value of -10 = 10  
console.log(Math.log(10)); // Natural log (base e) of 10  
console.log(Math.log2(8)); // Log base 2 of 8 = 3  
console.log(Math.log10(100)); // Log base 10 of 100 = 2  
console.log(Math.min(9,8,7)); // Minimum value = 7  
console.log(Math.max(9,8,7)); // Maximum value = 9  
console.log(Math.sin(Math.PI/2)); // Sine of 90 degrees (PI/2) = 1  
console.log(Math.cos(0)); // Cosine of 0 degrees = 1  
console.log(Math.tan(Math.PI/4)); // Tangent of 45 degrees (PI/4) = 1  
