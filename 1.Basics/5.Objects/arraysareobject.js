// Arrays are treated as objects in js 

var fruits = []

//you can add elements to the end of the array using the push() method, you can push multiple parameters to the push function
fruits.push("Apple", "Mango")
fruits.push("Banana", "Orange", "Grapes", "Papaya")
console.log(fruits)

// similarly you can remove the last element from an array using the pop() method\
fruits.pop()
fruits.pop()

console.log(fruits)